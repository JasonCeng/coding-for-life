void insertSort(T A[], int n) {
    int i,j;
    for(i = 2; j <= n; i++) { // 依次将A[2]~A[n]插入到前面已排序序列
        if(A[i] < A[i-1]) { // 若A[i]小于其前驱，则需将A[i]插入有序表
            A[0] = A[i];    // 复制为哨兵
            for(j = i-1; A[0] < A[j]; --j) {    // 从后往前查找带插入位置
                A[j+1] = A[j];  // 向后挪位
            }
            A[j+1] = A[0];  // 复制到插入位置
        }
    }
}